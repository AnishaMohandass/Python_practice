# Some useful functions in MATH module

import math

# fabs(x)
# Retruns the absolute value of x as a float
print(math.fabs(46))  # Output: 46.0
print(math.fabs(-8))  # Output: 8.0

# fmod(x, y)
# Returns the remainder when x is divided by y
print(math.fmod(5, 2))  # Output: 1.0

# pow(x, y)
# Returns x raised to the power y
print(math.pow(5, 3))  # Output: 125.0

# sqrt(x)
# Returns the square root of x
print(math.sqrt(256))  # Output: 16.0

# ceil(x)
# Rounds a number UP to the nearest integer
print(math.ceil(6.4))  # Output: 7

# floor(x)
# Rounds a number DOWN to the nearest integer
print(math.floor(6.4))  # Output: 6

# factorial(x)
# Returns the factorial of x
print(math.factorial(5))  # Output: 120